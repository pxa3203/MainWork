print("This program calculates the average of two numbers.")

#saving user inputs into variables as integers
number1 = int(input("Please enter the first number: "))
number2 = int(input("Please enter the second number: "))

#informing users of numbers they have entered
print("The numbers are", number1, "and", number2)

#displaying the average of the two numbers the user entered
print("The average is: ",(number1+number2)/2)